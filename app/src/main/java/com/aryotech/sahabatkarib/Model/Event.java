package com.aryotech.sahabatkarib.Model;

public class Event {

    String image;
    String tittle;

    public Event(){}

    public String getImage()
    {
        return this.image;
    }

    public String getTittle()
    {
        return this.tittle;
    }

    public void setImage(String image)
    {
        this.image = image;
    }

    public void setTittle(String tittle)
    {
        this.tittle = tittle;
    }

}
